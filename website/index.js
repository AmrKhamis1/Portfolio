    // Add animation to the skill bars on page load
    document.addEventListener('DOMContentLoaded', function() {
        const skills = document.querySelectorAll('.skill-bar-fill');
        skills.forEach(skill => {
          const width = skill.style.width;
          skill.style.width = '0';
          setTimeout(() => {
            skill.style.width = width;
          }, 100);
        });
      });


      function scrollingTO(where){
        const main = window.document.body;
        if(where == 'about'){
            main.scrollTo(0,800);

        }else if(where == 'projects'){
            main.scrollTo(0,1750);

        }else{
          main.scrollTo(0,3630);

        }

      }